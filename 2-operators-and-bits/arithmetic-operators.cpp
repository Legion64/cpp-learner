int main() {
    // Addition Operator (+)
    int num1 = 5 + 3;

    // Subtraction Operator (-)
    int num2 = 10 - 6;

    // Multiplication Operator (*)
    int num3 = 20 * 60;

    // Division Operator (/)
    int num4 = 20 / 2;
    float num4f = 60.1f / 24.7f;

    // Modulus Operator (%)
    int num5 = 64 % 3;

    // Increment Operator (++)
    int num6 = 77;
    int num6a = num6++; // num6 = 78, num6a = 77
    int num6b = ++num6; // num6 = 78, num6b = 78

    // Decrement Operator (--)
    int num7 = 46;
    int num7a = num7--; // num7 = 45, num7a = 46
    int num7b = --num7; // num7 = 45, num7b = 45

    return 0;
}
